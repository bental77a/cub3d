#include "libft.h"

int	ft_atoi(const char *str)
{
	long long	r;

	int i, (s);
	i = 0;
	s = 1;
	r = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' || str[i] == '\v'
		|| str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			s = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		r = r * 10 + (str[i] - '0');
		if (r > INT_MAX && s == 1)
			return (-1);
		else if (r > ((long long)INT_MIN * -1) && s == -1)
			return (0);
		i++;
	}
	return (r * s);
}
